package gravtitypackage;

public class Boulder {
	private int bx;
	private int by;
	
	public Boulder(int bx, int by)
	{
		this.bx = bx;
		this.by = by;		
	}

	public int getBx() {
		return bx;
	}

	public void setBx(int bx) {
		this.bx = bx;
	}

	public int getBy() {
		return by;
	}

	public void setBy(int by) {
		this.by = by;
	}
	
	
	

}
