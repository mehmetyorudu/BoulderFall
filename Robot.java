package gravtitypackage;

public class Robot {
	    private int x;
	    private int y;

	    public Robot(int x, int y) {
	        this.x = x;
	        this.y = y;
	    }

	    public int getRx() {
	        return x;
	    }

	    public int getRy() {
	        return y;
	    }

	    public void setRx(int x) {
	        this.x = x;
	    }

	    public void setRy(int y) {
	        this.y = y;
	    }

}
